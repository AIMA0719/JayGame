static constexpr int FONT_CHAR_COUNT = 95; // 32..126

bool TextRenderer::init() {
    int texW = ATLAS_TEX_W;
    int texH = ATLAS_TEX_H;
    std::vector<unsigned char> pixels(texW * texH * 4, 0);

    for (int idx = 0; idx < FONT_CHAR_COUNT; idx++) {
        int col = idx % ATLAS_COLS;
        int row = idx / ATLAS_COLS;
        int baseX = col * ATLAS_GLYPH_W;
        int baseY = row * ATLAS_GLYPH_H;

        for (int py = 0; py < ATLAS_GLYPH_H; py++) {
            unsigned char rowBits = FONT_5x7[idx][py];
            for (int px = 0; px < ATLAS_GLYPH_W; px++) {
                bool on = (rowBits >> (4 - px)) & 1;
                int texX = baseX + px;
                int texY = baseY + py;
                int offset = (texY * texW + texX) * 4;
                unsigned char val = on ? 255 : 0;
                pixels[offset + 0] = 255; // always white
                pixels[offset + 1] = 255;
                pixels[offset + 2] = 255;
                pixels[offset + 3] = val; // alpha controls visibility
            }
        }
    }

    glGenTextures(1, &fontTextureId_);
    glBindTexture(GL_TEXTURE_2D, fontTextureId_);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, texW, texH, 0,
                 GL_RGBA, GL_UNSIGNED_BYTE, pixels.data());
    glBindTexture(GL_TEXTURE_2D, 0);

    aout << "TextRenderer initialized: " << texW << "x" << texH
         << " font texture (id=" << fontTextureId_ << ")" << std::endl;

    return fontTextureId_ != 0;
}

void TextRenderer::shutdown() {
    if (fontTextureId_) {
        glDeleteTextures(1, &fontTextureId_);
        fontTextureId_ = 0;
    }
}

void TextRenderer::getCharUV(char c, float& u0, float& v0, float& u1, float& v1) const {
    int idx = static_cast<int>(c) - 32;
    if (idx < 0 || idx >= FONT_CHAR_COUNT) idx = 0; // space

    int col = idx % ATLAS_COLS;
    int row = idx / ATLAS_COLS;

    u0 = static_cast<float>(col * ATLAS_GLYPH_W) / static_cast<float>(ATLAS_TEX_W);
    v0 = static_cast<float>(row * ATLAS_GLYPH_H) / static_cast<float>(ATLAS_TEX_H);
    u1 = static_cast<float>((col + 1) * ATLAS_GLYPH_W) / static_cast<float>(ATLAS_TEX_W);
    v1 = static_cast<float>((row + 1) * ATLAS_GLYPH_H) / static_cast<float>(ATLAS_TEX_H);
}

void TextRenderer::drawText(SpriteBatch& batch, const std::string& text,
                             float x, float y, float scale,
                             Vec4 color, TextAlign align) const {
    if (fontTextureId_ == 0 || text.empty()) return;

    float totalW = measureText(text, scale);
    float startX = x;

    switch (align) {
        case TextAlign::Center: startX = x - totalW * 0.5f; break;
        case TextAlign::Right:  startX = x - totalW; break;
        case TextAlign::Left:   break;
    }

    float charW = (GLYPH_W + GLYPH_SPACING) * scale;
    float glyphW = GLYPH_W * scale;
    float glyphH = GLYPH_H * scale;
    float cx = startX;

    for (char c : text) {
        float u0, v0, u1, v1;
        getCharUV(c, u0, v0, u1, v1);

        batch.drawRawQuad(fontTextureId_,
                          cx, y, glyphW, glyphH,
                          u0, v0, u1 - u0, v1 - v0,
                          color.x, color.y, color.z, color.w);
        cx += charW;
    }
}

void TextRenderer::drawNumber(SpriteBatch& batch, int number,
                               float x, float y, float scale,
                               Vec4 color, TextAlign align) const {
    char buf[16];
    snprintf(buf, sizeof(buf), "%d", number);
    drawText(batch, buf, x, y, scale, color, align);
}

void TextRenderer::drawFloat(SpriteBatch& batch, float number,
                              float x, float y, float scale,
                              Vec4 color, TextAlign align) const {
    char buf[16];
    snprintf(buf, sizeof(buf), "%.1f", number);
    drawText(batch, buf, x, y, scale, color, align);
}

float TextRenderer::measureText(const std::string& text, float scale) const {
    if (text.empty()) return 0.f;
    return static_cast<float>(text.length()) * (GLYPH_W + GLYPH_SPACING) * scale
           - GLYPH_SPACING * scale;
}

float TextRenderer::measureNumber(int number, float scale) const {
    char buf[16];
    int len = snprintf(buf, sizeof(buf), "%d", number);
    return static_cast<float>(len) * (GLYPH_W + GLYPH_SPACING) * scale
           - GLYPH_SPACING * scale;
}

